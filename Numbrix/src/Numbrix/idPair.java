package Numbrix;

public class idPair {
	private int value;
	private double percent;
	
	public idPair(int v, double p){
		value = v;
		percent = p;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public double getPercent() {
		return percent;
	}

	public void setPercent(double percent) {
		this.percent = percent;
	}
	
	
}
